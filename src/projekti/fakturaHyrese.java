             public void fillCombo(){
                 try {
                     String query="select distinct nrFatures from fakturaDalese";
                     st=con.createStatement();
                     rs=st.executeQuery(query);
                     while(rs.next()){
                         int i = rs.getInt("nrFatures");
                         String e = Integer.toString(i);
                         fatura.addItem(e);
                     }
                 } catch (SQLException e) {
                     JOptionPane.showMessageDialog(this, e);
                 }
             }
             
             public void qmimiTotal(){
               try {
                   String sql = "SELECT SUM(qmimi) 'Sum'  FROM fakturadalese WHERE nrFatures='"+fatura.getSelectedItem()+"'";
                   pst=con.prepareCall(sql);
                   rs=pst.executeQuery();
                   while(rs.next()){
                        qmimi.setText(rs.getString("sum"));
                        Double i = Double.parseDouble(qmimi.getText());
                        Double t = (int)Math.round(i*100)/(double)100;
                        qmimi.setText(Double.toString(t)+"â‚¬");
                   }
                 } catch (SQLException e) {
                     JOptionPane.showMessageDialog(this, e);
                 }
             }
             
             public void totaliProdukteve(){
                 try {
                     String sql="SELECT SUM(`sasia`) 'Totali' FROM fakturadalese WHERE nrFatures='"+fatura.getSelectedItem()+"'";
                     pst=con.prepareCall(sql);
                     rs=pst.executeQuery();
                     while(rs.next()){
                        totaliProd.setText(rs.getString("totali"));
                        Double i = Double.parseDouble(totaliProd.getText());
                        double t = (int)Math.round(i*100)/(double)100;
                        totaliProd.setText(Double.toString(t));
                     }
                 } catch (SQLException e) {
                     JOptionPane.showMessageDialog(this, e);
                 }
             }
