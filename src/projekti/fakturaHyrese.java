    private void faturaPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_faturaPopupMenuWillBecomeInvisible
         try {
                String query = "select barkodi,emriIProduktit,qmimi,sasia,nrFatures,nrFiskal from fakturaDalese where nrFatures=?";
                DefaultTableModel model = (DefaultTableModel)tabela.getModel();
                pst = con.prepareStatement(query);
                pst.setString(1, fatura.getSelectedItem().toString());
                rs=pst.executeQuery();
                model.setRowCount(0);
                while(rs.next()){
                     Object [] row = new Object[6];
                     row[0] = rs.getInt("barkodi");
                     row[1] = rs.getString("emriIProduktit");
                     row[2] = rs.getDouble("qmimi");
                     row[3] = rs.getInt("sasia");
                     row[4] = fatura.getSelectedItem();
                     row[5] = rs.getInt("nrFiskal");
                     model.addRow(row);
                     
                }
                    qmimiTotal();
                    totaliProdukteve();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e);
                }
            
    }//GEN-LAST:event_faturaPopupMenuWillBecomeInvisible


